////////////////////////////////////////////////////////////////////////////////
// sum /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function sum(arr) {
var total = 0;
for(var i = 0; i < arr.length; i++){
  total += arr[i];
}
return total;
}

////////////////////////////////////////////////////////////////////////////////
// reverseArray ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArray(array) {
var arr = [];
for(var i = array.length -1; i >= 0; i--){
  arr.push(array[i]);
}
return arr;
}

