//////////////////////////////////////////////////////////////////////
// Function 2 - Keys to String ///////////////////////////////////////
//////////////////////////////////////////////////////////////////////

function keysToString(object) {
    var arr = [];
    for(var key in object){
        arr.push(key);
    }
    
    return arr.join(" ");
}

//////////////////////////////////////////////////////////////////////
// Function 3 - Values to String /////////////////////////////////////
//////////////////////////////////////////////////////////////////////

function valuesToString(object) {
    var arr = [];
    for(var key in object){
       if(typeof object[key] === 'string'){
        arr.push(object[key]);
       } 
        
    }
    
    return arr.join(" ");
}

